public class TestIfElse {
    public static void main(String[] args) {
        int x;
        x = 7;
        if (x > 5) {
            System.out.println(x);
        } else {
            System.out.println(0);
        }
        if (x < 3) {
            System.out.println(1);
        } else {
            System.out.println(2);
        }
    }
}
